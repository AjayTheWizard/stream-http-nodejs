const NodeType = Object.freeze({
  LISTOFNODES: "List_Of_Nodes",
  TEXTNODE: "Text_Node",
});

function h(
  tagname = "",
  attrs = {},
  events = {},
  children = [{ "": { attrs: {}, events: {}, children: "" } }],
  __Comp = false
) {
  if (Array.isArray(children)) {
    const hh = {};
    const domEvents = {};
    for (let i = 0; i < children.length; i++) {
      hh[Object.keys(children[i])[0]] = Object.values(children[i])[0];
    }
    let eventsKeys = Object.keys(events);
    for (let k = 0; k < eventsKeys.length; k++) {
      domEvents["on" + eventsKeys[k]] = events[eventsKeys[k]].toString();
    }
    return {
      [tagname]: {
        attrs,
        events,
        children: hh,
        nodeType: NodeType.LISTOFNODES,
      },
    };
  }

  if (typeof children === "string" || typeof children === "number") {
    return {
      [tagname]: {
        attrs,
        events,
        children,
        nodeType: NodeType.TEXTNODE,
      },
    };
  }
}

function ComponentRestPropsProcessor(rest) {
  const attrs = {};
  const events = {};
  const keys = Object.keys(rest);
  for (let i = 0; i < keys.length; i++) {
    if (keys[i].slice(0, 2) === "on") {
      events[keys[i].slice(2)] = rest[keys[i]].toString();
    } else {
      attrs[keys[i]] = rest[keys[i]];
    }
  }
  return {
    events,
    attrs,
  };
}

function Button(
  { children, ...rest } = {
    children: [],
  }
) {
  const { attrs, events } = ComponentRestPropsProcessor(rest);
  return h("button", attrs, events, children);
}

function Heading(
  { children, type, ...rest } = {
    children: [],
    type: "",
  }
) {
  const { attrs, events } = ComponentRestPropsProcessor(rest);
  return h(HeadingType[type] ?? "p", attrs, events, children);
}

function Div(
  { children, ...rest } = {
    children: [],
  }
) {
  const { attrs, events } = ComponentRestPropsProcessor(rest);
  return h("div", attrs, events, children);
}

